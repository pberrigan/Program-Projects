// Program 1: This program will calculate the sum of two numbers and display the result.


#include <iostream>
using namespace std;

int main()
{
    // Variable Declaration
    int num1, num2, total;

    // Input
    num1 = 50;
    num2 = 100;

    // Processing
    total = num1 + num2;

    // Output
    cout << "The total is: " << total << endl;

    return 0;
}
